void encoder_update_user(uint8_t index, bool clockwise) {
  if (muse_mode) {
    if (IS_LAYER_ON(_RAISE)) {
      if (clockwise) {
	muse_offset++;
      } else {
	muse_offset--;
      }
    } else {
      if (clockwise) {
	muse_tempo+=1;
      } else {
	muse_tempo-=1;
      }
    }
  } else {
    if (clockwise) {
      register_code(KC_PGDN);
      unregister_code(KC_PGDN);
    } else {
      register_code(KC_PGUP);
      unregister_code(KC_PGUP);
    }
  }
}

void dip_switch_update_user(uint8_t index, bool active) {
    switch (index) {
	case 0:
	    if (active) {
		layer_on(_ADJUST);
	    } else {
		layer_off(_ADJUST);
	    }
	    break;
	case 1:
	    if (active) {
		muse_mode = true;
	    } else {
		muse_mode = false;
	    }
    }
}


