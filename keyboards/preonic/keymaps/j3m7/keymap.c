bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
	case LOWER:
	  if (record->event.pressed) {
	    layer_on(_LOWER);
	    update_tri_layer(_LOWER, _RAISE, _ADJUST);
	  } else {
	    layer_off(_LOWER);
	    update_tri_layer(_LOWER, _RAISE, _ADJUST);
	  }
	  return false;
	  break;
	  
	case RAISE:
	  if (record->event.pressed) {
	    layer_on(_RAISE);
	    update_tri_layer(_LOWER, _RAISE, _ADJUST);
	  } else {
	    layer_off(_RAISE);
	    update_tri_layer(_LOWER, _RAISE, _ADJUST);
	  }
	  return false;
	  break;

  case MC_HELP:
    if (record->event.pressed) {
      int i = 0;
      char c = 0;
      int n = 0;
      
      while (true) {
	n = RLE_INSTRUCTIONS[i];
	
	if( n == 0) {
	  break;	  
	}
	
        c = RLE_INSTRUCTIONS[i+1];
	i += 2;
	
	for (int j=0; j<n; j++) {
	  send_char(c);	  
	}
      }
      
      return true;
      break;
    }
  }
  return true;
};

